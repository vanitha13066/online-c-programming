#include<stdio.h>
int main()
{
	int a,b;
	scanf("%d%d",&a,&b);
	printf("%d and %d addition is %d\n",a,b,a+b);
	printf("%d and %d substraction is %d\n",a,b,a-b);
	printf("%d and %d multiplication is %d\n",a,b,a*b);
	printf("%d and %d division is %d\n",a,b,a/b);
	printf("%d and %d modulus is %d",a,b,a%b);
	return 0;
}
