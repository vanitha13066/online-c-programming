//Assignment : =,+=,-=,*=,/=,%=
#include<stdio.h>
int main()
{
	int a=5;
	int b=a;  //b=5
	b+=a; // b=b+a=5+5=10
	printf("value is:%d",b);
	
	return 0;
}
